#operators

#addition
x=5
x=x+2 #same as x+=2
print(x)


#substraction
x=3
x=x-3 #same as x-=3

#multiplication
x=2
x=x*4 #same as x*=4

#division
x=8
x=x/3 #same as x/=3 (output = 2.666666)
#division without remainder
x=x//3 #same as x//=3 (output =2)


#moduls
x=10
x=x%3 #same as x%=3 (output = 1)

#Exponentiation
x=3
x=x**3 #or x**=3 (output=27)


#Python Logical Operators
#binary and, or, xor, not (&,|,^,~)
x=x&3
x=x^3
x=x|3

